class HADARNet(nn.Module):
    def __init__(self, out_channels=6, args=None):
        super().__init__()
        self.in_channels = args.output_nc
        self.out_channels = out_channels
        smp_model = args.smp_model
        smp_encoder = args.smp_encoder
        smp_encoder_weights = args.smp_encoder_weights
        if smp_encoder_weights == 'None':
            smp_encoder_weights = None
        self.tevnet = getattr(smp, smp_model)(encoder_name=smp_encoder,
                                              encoder_weights=smp_encoder_weights,
                                              in_channels=self.in_channels,
                                              classes=self.out_channels,
                                              )

    def forward(self, x):
        preds = self.tevnet(x)
        preds[:, 0, :, :] = torch.sigmoid(preds[:, 0, :, :])
        preds[:, 1, :, :] = F.relu(preds[:, 1, :, :])
        return preds


class GANLoss_My(nn.Module):
    """Define different GAN objectives.

    The GANLoss class abstracts away the need to create the target label tensor
    that has the same size as the input.
    """

    def __init__(self, gan_mode, target_real_label=1.0, target_fake_label=0.0,use_FD=False):
        """ Initialize the GANLoss class.

        Parameters:
            gan_mode (str) - - the type of GAN objective. It currently supports vanilla, lsgan, and wgangp.
            target_real_label (bool) - - label for a real image
            target_fake_label (bool) - - label of a fake image

        Note: Do not use sigmoid as the last layer of Discriminator.
        LSGAN needs no sigmoid. vanilla GANs will handle it with BCEWithLogitsLoss.
        """
        super(GANLoss_My, self).__init__()
        self.register_buffer('real_label', torch.tensor(target_real_label))
        self.register_buffer('fake_label', torch.tensor(target_fake_label))
        self.gan_mode = gan_mode
        self.use_FD = use_FD
        if gan_mode == 'lsgan':
            self.loss = nn.MSELoss()
        elif gan_mode == 'vanilla':
            self.loss = nn.BCEWithLogitsLoss()
        elif gan_mode in ['wgangp']:
            self.loss = None
        else:
            raise NotImplementedError('gan mode %s not implemented' % gan_mode)

    def get_target_tensor(self, prediction, target_is_real):
        """Create label tensors with the same size as the input.

        Parameters:
            prediction (tensor) - - tpyically the prediction from a discriminator
            target_is_real (bool) - - if the ground truth label is for real images or fake images

        Returns:
            A label tensor filled with ground truth label, and with the size of the input
        """

        if target_is_real:
            target_tensor = self.real_label
        else:
            target_tensor = self.fake_label
        return target_tensor

    def __call__(self, prediction, target_is_real):
        """Calculate loss given Discriminator's output and grount truth labels.

        Parameters:
            prediction (tensor) - - tpyically the prediction output from a discriminator
            target_is_real (bool) - - if the ground truth label is for real images or fake images

        Returns:
            the calculated loss.
        """
        if isinstance(prediction[0], list):
            loss = 0
            num = 0
            if self.use_FD:
                for input_i in prediction[:-1]:
                    pred = input_i[-1]
                    target_tensor = self.get_target_tensor(pred, target_is_real)
                    target_tensor = target_tensor.expand_as(pred)
                    loss += self.loss(pred, target_tensor)
                    num += 1


                loss = loss / num
                input_i = prediction[-1]
                pred = input_i[-1]
                target_tensor = self.get_target_tensor(pred, target_is_real)
                target_tensor = target_tensor.expand_as(pred)
                loss += self.loss(pred, target_tensor)

            else:
                for input_i in prediction:
                    pred = input_i[-1]
                    target_tensor = self.get_target_tensor(pred, target_is_real)
                    target_tensor = target_tensor.expand_as(pred)
                    loss += self.loss(pred, target_tensor)
                    num += 1

                loss = loss / num
            return loss
        else:
            target_tensor = self.get_target_tensor(prediction[-1], target_is_real)
            return self.loss(prediction[-1], target_tensor)


class GANLoss(nn.Module):
    """Define different GAN objectives.

    The GANLoss class abstracts away the need to create the target label tensor
    that has the same size as the input.
    """

    def __init__(self, gan_mode, target_real_label=1.0, target_fake_label=0.0):
        """ Initialize the GANLoss class.

        Parameters:
            gan_mode (str) - - the type of GAN objective. It currently supports vanilla, lsgan, and wgangp.
            target_real_label (bool) - - label for a real image
            target_fake_label (bool) - - label of a fake image

        Note: Do not use sigmoid as the last layer of Discriminator.
        LSGAN needs no sigmoid. vanilla GANs will handle it with BCEWithLogitsLoss.
        """
        super(GANLoss, self).__init__()
        self.register_buffer('real_label', torch.tensor(target_real_label))
        self.register_buffer('fake_label', torch.tensor(target_fake_label))
        self.gan_mode = gan_mode
        if gan_mode == 'lsgan':
            self.loss = nn.MSELoss()
        elif gan_mode == 'vanilla':
            self.loss = nn.BCEWithLogitsLoss()
        elif gan_mode in ['wgangp']:
            self.loss = None
        else:
            raise NotImplementedError('gan mode %s not implemented' % gan_mode)

    def get_target_tensor(self, prediction, target_is_real):
        """Create label tensors with the same size as the input.

        Parameters:
            prediction (tensor) - - tpyically the prediction from a discriminator
            target_is_real (bool) - - if the ground truth label is for real images or fake images

        Returns:
            A label tensor filled with ground truth label, and with the size of the input
        """

        if target_is_real:
            target_tensor = self.real_label
        else:
            target_tensor = self.fake_label
        return target_tensor.expand_as(prediction)

    def __call__(self, prediction, target_is_real):
        """Calculate loss given Discriminator's output and grount truth labels.

        Parameters:
            prediction (tensor) - - tpyically the prediction output from a discriminator
            target_is_real (bool) - - if the ground truth label is for real images or fake images

        Returns:
            the calculated loss.
        """
        if self.gan_mode in ['lsgan', 'vanilla']:
            target_tensor = self.get_target_tensor(prediction, target_is_real)
            loss = self.loss(prediction, target_tensor)
        elif self.gan_mode == 'wgangp':
            if target_is_real:
                loss = -prediction.mean()
            else:
                loss = prediction.mean()
        return loss


class TevLoss(nn.Module):
    """Define different GAN objectives.

    The GANLoss class abstracts away the need to create the target label tensor
    that has the same size as the input.
    """

    def __init__(self, opt):
        """ Initialize the GANLoss class.

        Parameters:
            gan_mode (str) - - the type of GAN objective. It currently supports vanilla, lsgan, and wgangp.
            target_real_label (bool) - - label for a real image
            target_fake_label (bool) - - label of a fake image

        Note: Do not use sigmoid as the last layer of Discriminator.
        LSGAN needs no sigmoid. vanilla GANs will handle it with BCEWithLogitsLoss.
        """
        super(TevLoss, self).__init__()
        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        self.Tev = HADARNet(args=opt).to(self.device)
        self.Tev = nn.DataParallel(self.Tev, device_ids=[0])
        self.checkpoint = torch.load(opt.tev_checkpoint_dir, map_location=torch.device('cpu'), weights_only=True)
        self.Tev.load_state_dict(self.checkpoint['state_dict'])
        self.loss_tev_weight = opt.loss_tev_weight
        self.loss_rec_weight = opt.loss_rec_weight

    def forward(self, IR_pred, IR_true):
        self.Tev.eval()
        for param in self.Tev.parameters():
            param.requires_grad = False
        x_pred_decode = torch.clamp((IR_pred + 1.0) / 2.0, min=0.0, max=1.0)
        x_true_decode = torch.clamp((IR_true + 1.0) / 2.0, min=0.0, max=1.0)
        # Tev loss
        preds_T = self.Tev(x_pred_decode)
        true_T = self.Tev(x_true_decode)
        loss_tev = F.mse_loss(preds_T, true_T, reduction='none')
        # channels 1、2、3
        loss_tev = loss_tev.mean([1, 2, 3])
        # Rec loss
        HADARloss = HARDAloss(vnums=4)
        loss_rec = HADARloss.loss_rec(preds_T, x_pred_decode)
        self.loss = self.loss_tev_weight*loss_tev.mean() + self.loss_rec_weight*loss_rec.mean()
        return self.loss


class HARDAloss():
    def __init__(self, vnums=4, loss_type='MSE'):
        self.vnums = vnums
        if loss_type == 'L1':
            self.loss = nn.L1Loss()
        elif loss_type == 'MSE':
            self.loss = nn.MSELoss()
        else:
            pass

    def loss_rec(self, preds, x):
        x_mean = torch.mean(x, dim=1).unsqueeze(1)
        rec_img = self.rec(preds, x)
        loss = self.loss(rec_img, x_mean)
        return loss

    def rec_e(self, preds):
        e = preds[:, 0, :, :].unsqueeze(1)
        return e

    def rec_T(self, preds):
        T = preds[:, 1, :, :].unsqueeze(1)
        return T

    def rec_env(self, preds, x_mean):
        b, _, h, w = preds.shape
        V = preds[:, 2:2 + self.vnums, :, :]
        h_split_nums = int(math.sqrt(self.vnums))
        w_split_nums = self.vnums // h_split_nums
        assert h_split_nums * w_split_nums == self.vnums
        x_beta = F.avg_pool2d(x_mean, (h // h_split_nums, w // w_split_nums)).reshape(b, 1, self.vnums)
        v_pred = V.reshape(b, self.vnums, h * w)
        env = torch.matmul(x_beta, v_pred)
        env = env.view(b, 1, h, w)
        return env

    def rec(self, preds, x):
        x_mean = torch.mean(x, dim=1)
        e = self.rec_e(preds)
        T = self.rec_T(preds)
        env = self.rec_env(preds, x_mean)
        rec_img = torch.mul(e, T) + torch.mul(1 - e, env)
        return rec_img

class Freq_Loss(nn.Module):
    def __init__(self):
        super(Freq_Loss, self).__init__()
        self.criterion = nn.MSELoss()

    def forward(self, x, y):
        x_fft = torch.fft.fft2(x, dim=(-2, -1))

        x_fft_real = x_fft.real
        x_fft_imag = x_fft.imag
        y_fft = torch.fft.fft2(y, dim=(-2, -1))

        y_fft_real = y_fft.real
        y_fft_imag = y_fft.imag
        loss_real = self.criterion(x_fft_real, y_fft_real)
        loss_imag = self.criterion(x_fft_imag, y_fft_imag)
        return loss_real + loss_imag

class VGGLoss(nn.Module):
    def __init__(self):
        super(VGGLoss, self).__init__()
        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        # self.vgg = Vgg19().cuda()
        self.vgg = Vgg19().to(self.device)
        self.criterion = nn.L1Loss()
        self.weights = [1.0/32, 1.0/16, 1.0/8, 1.0/4, 1.0]

    def forward(self, x, y):
        x_vgg, y_vgg = self.vgg(x), self.vgg(y)
        loss = 0
        for i in range(len(x_vgg)):
            loss += self.weights[i] * self.criterion(x_vgg[i], y_vgg[i].detach())
        return loss
